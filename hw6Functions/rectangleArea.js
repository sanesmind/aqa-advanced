function calculateAreaOfRectangle(width, height) {
    return width * height;
}

console.log(calculateAreaOfRectangle(5, 10));

/////////////////////////////////////////////////////////////////////
const calcAreaOfRectExpression = function (width, height) {
    return width * height;
}

console.log(calcAreaOfRectExpression(6, 10));


////////////////////////////////////////////////////////////////////
const calcAreaOfRectArrow = (width, height) => {
    return width * height;
}

console.log(calcAreaOfRectArrow(7, 10));